
pares = [2,4,6,8,10]

print(pares[0]) #Apresenta o primeiro elemento da lista
print(pares[3]) #Apresenta o quarto elemento da lista
print(pares[4]) #Apresenta o quinto elemento da lista
print(pares[-1])#-1 apresenta o ultimo item da lista
#print(pares[6]) #Erro pois a lista não tem o elemento 6

print(pares[1:4]) #Apresenta a lista do indice 1 até o indice 3(o 4 não esta incluso pois é [i:j-1])
print(pares[:3]) #Apresenta os elementos do primeiro até o segundo indice, ou seja [2,4,6]
print(pares[2:]) #Apresenta os elementos do indice 2 até o final
print(pares[:]) #Seleciona todos os elementos da lista
